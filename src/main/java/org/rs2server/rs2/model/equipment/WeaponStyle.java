package org.rs2server.rs2.model.equipment;
/**
 * The types of combat weapons
 * @author Sir Sean
 *
 */
public enum WeaponStyle {
	
	/**
	 * Range type weapons
	 */
	RANGED,
	/**
	 * Range type weapons
	 */
	RANGE,
	
	/**
	 * Magic type weapons
	 */
	MAGIC,
	
	/**
	 * Melee type weapons
	 */
	MELEE,
	
	/**
	 * Weapon has a special attack/effect
	 */
	SPECIAL,

	/**
	 * Unset weapons
	 */
	NONE;
}
